    </h3>
  <p>
  Ameria es una chica poco común. Está totalmente obsesionada con la justicia, y cree que el simple hecho de representar el bien es suficiente para vencer al mal. Le gusta subirse a lugares altos para reprochar sus crímenes a los malhechores y anunciar que les va a castigar. Parte de esta faceta se la debe que su padre también es así, y al hecho de haber nacido en Saillune, una tierra que ha visto nacer a grandes héroes de la historia. Aparte de todo eso es una chica simpática, alegre, valiente, decidida, y de buen corazón.

  </p>
<p>
Ameria es la segunda hija de Philionel El de Saillune, príncipe regente del Reino de Saillune. Siendo muy joven, perdió a su madre a manos del asesino Booley. Gracia, su hermana mayor, presenció la escena y decidió irse de Saillune por razones no del todo conocidas, aunque supuestamente para hacerse más fuerte. Ameria es una sacerdotisa de pleno derecho, y la tercera en la línea sucesora del reino, detrás de su hermana y de su padre. Participa activamente en los asuntos de palacio, actuando como emisaria de paz del reino ante otros países, y representando a Saillune en distintos eventos, aunque su mayor pasión es impartir justicia en nombre del bien.
    </p>
    <p>
    En las novelas conoce a Lina y Gourry cuando estos escoltan a Sylphiel a la ciudad de Saillune. Su padre, el príncipe Phil, les pide ayuda para resolver el caso de los continuos intentos de asesinato hacia su persona, y Ameria colabora con ellos para descubrir a los verdaderos culpables: su primo Alfred y el mazoku Kanzel. Tras esta aventura se une al grupo, pues siente que los mazoku están tramando algo importante, y quiere ayudar a resolverlo. Esto supuso para Ameria la oportunidad de vivir aventuras impartiendo justicia, enfrentándose a la secta de Krotz, a una bestia Zanaffer, e incluso a Chaos Dragon Garv y Hellmaster Phibrizzo, cuyo plan para destruir el mundo fue detenido a tiempo gracias a Lina Inverse, Ameria y los demás. A partir de entonces, Ameria se separa de sus amigos para informar a Saillune y al resto del mundo sobre el hecho de que los mazoku de clase alta son reales, y no simples leyendas, por lo que es necesario preparar a la humanidad. No se reencuentra con los demás hasta mucho tiempo después, cuando les ayuda a derrotar al grupo de elfos conocido como Forest Hound. Durante sus viajes, Ameria conoce a muchos nuevos amigos interesantes, como Zelgadiss y Sylphiel, y aprende que la justicia no es lo mismo para todo el mundo. Aun así, nada es capaz de cambiar su espíritu de lucha y su fe en el poder del bien.    <h3>
        Habilidades

    </h3>
    <p>
    Ameria es una sacerdotisa blanca, por lo que está especializada en este tipo de magia, dominando hechizos tan poderosos como el Resurrection y el Megido Flare. Aun así, su espíritu luchador le animó a estudiar magia shamanística y negra junto a su hermana Gracia. Es capaz de conjurar el La Tilt y ha creado varios hechizos propios, como el Vis Farank, que concentra energía astral en sus puños. Además, puede utilizar algunos conjuros de Magia Negra, como el Dynast Brass y el Blast Ash. Es muy raro ver a Ameria utilizar armas, pero es una luchadora cuerpo a cuerpo muy habilidosa, combinando velocidad, agilidad y destreza.</p> 
