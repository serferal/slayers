       <h2>
           リナ＝インバース (rina inbaasu)
       </h2>
       <h3>
           Otros alias:
           · Asesina de ladrones (kobber killer).
           · La que aterroriza a los dragones (dra-mata).
           · La enemiga de todo lo que vive.
           · Lina la Rosa.
           Raza: Humana.
           Edad: 15-19 años (a lo largo de la serie).
           Lugar de procedencia: Zepheel, reino de Zephiria.

           Parafernalia:
           · Daga.
           · Espada corta.
           · Talismán Demon Blood.
           · Cinta protectora.
           · Algunas de las gemas en su traje son talismanes protectores.

       </h3>
       <p>
           Lina Inverse no es precisamente la típica jovencita, sino que es bruta, maleducada, comilona y algo acomplejada por su corto desarrollo, especialmente el tamaño de sus pechos. Sus motivaciones son el dinero y la comida, aunque tiene buen corazón. Su hermana Luna le causa verdadero terror, hasta el punto de que su sentimiento de inferioridad con respecto a su hermana es el motivo de que aprendiese magia.
       </p>

       <p>
           Lina es hija de un mercenario y una hechicera, ambos retirados y asentados en una pequeña tienda de comestibles, en Zepheel. Cuando aún tenía 13 años, decidió sacarse algún dinero, y se dedicó a proyectar sobre la niebla, y con espejos, una imagen de su hermana Luna mientras se duchaba. Cuando Luna se enteró, Lina decidió huir de casa, supuestamente por el consejo de su hermana de "ver el mundo", y así aprender cosas nuevas y para conseguir tesoros, por supuesto. Entonces fue cuando se encontró con Naga, una "rival" con la que competiría por todo, y con la que trabajaría en ocasiones, metiéndose en muchos lios.


       </p>
       <p>
           Por razones aún desconocidas, Naga y Lina se separaron. Más tarde, a la edad de 15 años, Lina conoció a Gourry Gabriev tras vencer a una banda de ladrones, y se hicieron compañeros, amigos y algo más. Pronto encontraron más aliados que, junto a ellos, vivirían grandes aventuras y peligros, enfrentándose a todo tipo de enemigos, desde bestias legendarias al mismísimo rey de los mazoku. Además, Lina es la única humana que ha visitado la Biblia Claire original, y la responsable de que Lord of Nightmares fuera invocado en la Tierra.
       </p>

       <h3>
           Habilidades

       </h3>
       <p>
           Lina tiene prácticamente la mayor capacidad mágica que puede tener un ser humano. Además, es lo que podríamos llamar una genio de la magia, pues es capaz de dominar los hechizos más poderosos, modificarlos, y hasta crear los suyos propios. Sin ir más lejos, el conjuro más poderoso que existe, el Giga Slave, es creación suya, siendo un conjuro que usa el poder del ser creador de todo el universo, Lord of Nightmares. A la edad de 17 ya se había convertido en la responsable directa de la muerte de los demonios más poderosos entre los Mazoku, incluídas dos de las siete partes del Dark Lord Sabranigudu.

       </p>
