    <h2>
        ゼルガディス＝グレイワーズ (zerugadisu gureiwaazu) </h2>
    <h3>

        Otros alias:
        · El extraño espadachín.
        · Guerrero demoníaco.
        Raza: Humano (transformado en una quimera).
        Edad: 20-24 años aproximadamente (a lo largo de la serie).
        Lugar de procedencia: Desconocido.

        Parafernalia:
        · Espada larga.
        · Dagas.
    </h3>
    <p>
        Zelgadiss es el solitario y reservado del grupo. Es un chico algo introvertido, y le cuesta abrirse a la gente, debido a que su aspecto siempre despierta miradas curiosas y recelosas, por lo que normalmente tapa su pelo y su cuerpo casi por completo. Por otro lado es una persona en la que se puede confiar, y muy concienzudo, culto e inteligente, aunque muy tímido. Como es de esperar, no soporta muy bien las bromas que los demás puedan hacer sobre su cuerpo.

    <p>
        Zelgadiss es el nieto de Rezo, el Monje Rojo. Sus padres viajaban con el sabio por todo el continente, pero por algún motivo empezaron a temerle y se escaparon de su lado. Debido a que no paraban de viajar, no se sabe dónde nació Zelgadiss, más tarde murieron un accidente y Zelgadiss quedó al cuidado de su abuelo. Siempre había sido un gran espadachín que apenas había empezado a estudiar magia, pero había llegado el tope de sus habilidades cuando Rezo le propuso darle más poder. Zelgadiss aceptó sin pensar en las consecuencias, y fue convertido en una quimera de humano, brow demon, y golem de roca.
    </p>
    <p>
    A partir de entonces Zelgadiss se convirtió en mercenario a las órdenes de su abuelo, que pronto le embarcaría en la búsqueda de la poderosa Piedra Filosofal, que Rezo buscaba para recuperar la vista. Por coincidencias del destino, la piedra llegó a las manos de Lina Inverse, lo que unió el destino de Zel al de ella. Tras traicionar a Rezo en venganza por su transformación, Zelgadiss viajó junto a Lina y los demás, viviendo grandes aventuras y batallas, intentando siempre encontrar una cura para su cuerpo, y volver a ser humano, algo que aún no ha conseguido.
    <h3>
        Habilidades

    </h3>
    <p>
    Zelgadiss tiene muchas y variadas habilidades, algunas debido al esfuerzo y el entrenamiento, y otras gracias a su peculiar cuerpo. Para empezar es un gran espadachín que, si bien no llega al nivel de su compañero Gourry Gabriev, no es precisamente un novato. Es muy hábil con la magia, habiéndose especializado en magia shamanística de todos los tipos, y siendo capaz de usar el hechizo astral más poderoso, el Ra Tilt. Su parte de gólem de roca le confiere una resistencia y fuerza física mayores a las de un humano corriente, mientras que su parte de brow demon, que es una criatura mágica, no un mazoku, le confiere una capacidad mágica superior a la humana. Esto no quiere decir que pueda usar hechizos más fuertes, sino que puede utilizar magia durante más tiempo sin cansarse que un humano corriente. Además su cuerpo envejece a la mitad de la velocidad de un ser humano, lo que le confiere el doble de la esperanza de vida normal.    </p>
