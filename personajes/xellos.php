    <h2>
        ゼロス (zerosu)

    </h2>
    <h3>

        Otros alias:
        · Sacerdote de Greater Beast Zelas Metallium.
        · Asesino de dragones.
        Raza: Mazoku.
        Edad: Más de 1000 años.
        Lugar de procedencia: Inaplicable (¿plano astral?).

        Parafernalia:
        · Talismán Demon Blood.
        · La ropa y el bastón que lleva son parte de su propio cuerpo.
    </h3>
    <p>
    Xellos tiene una personalidad muy peculiar. A primera vista parece un chico alegre, sociable y bromista, pero en realidad es un ser muy manipulador, capaz de urdir los más complicados de los planes para que la gente haga lo que él (o su señor) quiera. Es un mazoku, por lo tanto es un ser que se alimenta de los sentimientos negativos y del dolor. Por ello, pese a las apariencias, Xellos es un ser capaz de ser muy cruel y malvado, pues esa es su naturaleza.
    </p>
    <p>
    Xellos no es cualquier Mazoku, es el Sacerdote de Greater Beast Zelas Metallium, uno de los subordinados más poderosos de Shabranigudu. Los demás subordinados crearon un sacerdote y un general durante la Guerra del Descenso del Demonio para que siguieran sus órdenes, pero Zelas Metallium creó a un único sirviente. Durante dicha guerra, Xellos fue el responsable de la casi total extinción de los Dragones Dorados, ganándose el título de "Asesino de Dragones".
    </p>
    <p>
    Su relación con Lina Inverse y los demás comenzó cuando Phibrizzo quiso utilizar a Lina para destruir el mundo. Phibrizzo no tenía sirvientes por lo que "pidió prestado" a Xellos de Zelas Metallium. Desde entonces, y por diferentes motivos, Xellos siempre ha estado relacionado con el grupo, que lo toleran pues saben que es imposible librarse de él. Sea como sea, la compañía de Xellos nunca es por casualidad, y siempre es preludio de una dura batalla por sus vidas. Aunque por lo menos Lina fue capaz de sacar algo de provecho de Xellos, consiguiendo que le vendiera el talismán Demon Blood.
    <h3>
        Habilidades

    </h3>
    <p>
    Xellos es el Mazoku más poderoso por debajo de los cinco subordinados directos de Shabranigudu. Cuando los Lords crearon a sus sirvientes, dividieron el poder entre 2 o más de ellos, mientras que Zelas utilizó el mismo poder para crear a un único sacerdote. Si bien su poder no es suficiente para enfrentarse a ninguno de los lords, es casi invencible, aunque el Caballero de Ceiphied podría plantarle cara. Su forma real es la de un cono de energía oscura rodeado por otros conos más pequeños que utiliza para atacar. Todos estos conos, y el bastón que utiliza en su forma humana, son parte de su cuerpo  </p>
