        ガウリイ＝ガブリエフ (gaurii gaburiefu)
    </h2>
    <h3>
        Otros alias:
        · El guardián de Lina.
        Raza: Humano.
        Edad: 18-22 años (a lo largo de la serie).
        Lugar de procedencia: Imperio Elmekia.

        Parafernalia:
        · Gorn Nova (Espada de Luz).
        · Blast Sword.
    </h3>
    <p>
        Gourry Gabriev es el espadachín del grupo, y es algo que hace bastante bien. Es un chico honesto, honrado y de buen corazón... pero no es precisamente el más brillante. Es simpático y divertido, y tiene la memoria de un mosquito. No es capaz de recordar hechos o personas que conoció hace poco, y normalmente no entiende la situación en la que el grupo se encuentra, dejándose llevar por Lina. Curiosamente, cuando está borracho, se comporta muy galantemente, aunque cuando se recupera no recuerda nada de lo ocurrido. </p>

    <p>
        Gourry es descendiente del guerrero que destruyó a la bestia Zanaffer en Sairaag hace 120 años, y por lo tanto su familia es la poseedora de la poderosa Espada de Luz. Cansado de las disputas familiares que dicha espada causaba, se marchó de casa dispuesto a tirarla a un lago. Allí se encuentra con un hombre que le convence para que no lo haga, y que haga algo bueno con ella, juntos tienen una batalla para proteger una aldea de los mazoku, y Gourry entiende que la espada no es la causa de sus problemas.

    </p>
    <p>
        Justo después ocurrió algo que cambiaría su vida para siempre. Conoció a Lina Inverse, a la que confundió con una jovencita en apuros, por lo que se convirtió en su guardián. Esto no haría sino complicar su vida, llenándola con aventuras, peligros y batallas contra bestias legendarias como la que venció su antepasado, hechiceros con ansias de grandeza, y algunos de los mazoku más poderosos que existen. Siempre en compañía de su protegida Lina, y los compañeros que han ido encontrando en el camino. </p>

    <h3>
        Habilidades

    </h3>
    <p>
        Gourry es el mejor espadachín que aparece en Slayers. Su manejo de la espada y las artes de lucha en general es envidiable, transformándose totalmente del típico despreocupado a un guerrero aplicado y totalmente concentrado. Si bien como humano normal no puede utilizar todo el poder de la Espada de Luz, es capaz de utilizarla increiblemente bien. Aunque ha tenido que luchar sin ella tras perderla, Gourry ha demostrado ser capaz de utilizar las habilidades de cualquier espada hasta el límite de su capacidad. De por sí tiene una fuerza y agilidad increibles.

    </p>
