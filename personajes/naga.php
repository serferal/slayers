       </h3>
       <p>
       Naga puede llegar a ser una persona muy difícil de soportar. Es engreida, mentirosa, egoísta y de risa insoportable. Siempre hará lo que sea para salirse con la suya, pero al mismo tiempo es muy inocente para otras cosas, cayendo a veces en confusiones simplemente por la apariencia de la gente, o siendo engañada por alguien con muy mala pinta, porque "no hay que juzgar un libro por la portada". Aparte de eso, hay que decir que es muy alegre y optimista, algo muy loable en ella tras todo por lo que ha pasado.       </p>

       <p>
       El verdadera identidad es la de Gracia Ul Naga Saillune, hermana de Ameria e hija primogénita del príncipe Philionel. Cuando tenía unos 16 ó 17 años, el asesino Booley entró en palacio con intención de matar a Gracia, pero mató a su madre por error, delante de la joven. Naga mató al asesino con el Chaos String, y decidió dejar el castillo para viajar por el mundo y ser más fuerte, aunque desde entonces se desmaya al ver la sangre. Durante este tiempo ha mantenido contacto con su familia a través de mensajeros, lo que también les permite mandarle dinero de vez en cuando.

       </p>
       <p>
       Tiempo después conocería a Lina Inverse y se autoproclamaría su rival, tratando de competir con ella en todo para demostrar que es superior a Lina en todos los aspectos. De hecho, en muchas ocasiones han luchado "a muerte", aunque Naga suele ponerse del lado vencedor con bastante rapidez... La razón por la que Naga y Lina se separaron aún es desconocida, ya que poco tiempo después Lina conoció a Gourry y el resto del grupo, y Naga no les acompaña. De hecho Lina no es consciente que Ameria y Naga son parientes, puesto que Naga nunca le reveló su verdadera identidad.
       </p>


       <h3>
           Habilidades

       </h3>
       <p>
Naga es una poderosa hechicera con prácticamente la máxima capacidad mágica de un humano corriente, el mismo nivel que Lina Inverse. Aún así Naga es menos adepta a los conjuros super poderosos que usa Lina, pero sería capaz de utilizarlos. Es muy hábil con los conjuros de agua y las invocaciones de criaturas (como zombies o gólems), y es muy dada a crear nuevos conjuros o modificar algunos ya existentes. Además tiene la habilidad natural, según ella, de percibir la presencia de medusas...


