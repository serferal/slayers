       <p>
              Esta segunda temporada vería la luz un año después de la primera. Las primeras imagenes publicitarias la llamaban Slayers 2, pero al final recibió el nombre de Slayers Next. Adapta de la saga principal de novelas, la segunda (Hechicero de Atlas), la cuarta (Batalla en Saillune), la séptima (El Desafío de Garv), y la octava (Rey de la Ciudad Fantasma), además de añadir personajes y hechos de la quinta novela (La Bestia Mágica Plateada).

              Al igual que con la primera temporada, se añadieron historias de relleno, y personajes y lugares exclusivos del anime. La calidad general del dibujo, animación y música mejoró muchísimo, incluyendo incluso algo de animación CGI para el opening, algo que por aquel entonces era toda una novedad. Los episodios de esta temporada empiezan todos con la última sílaba del título del episodio anterior, excepto el primero, claro, y por primera vez cuentan con "eyecatches" o cortinillas para las pausas publicitarias. En esta ocasión Lina no tiene un texto introductorio antes del opening pero sí se mantiene la tradición de que, al final de cada episodio, Lina resume brevemente el episodio siguiente, terminando con la frase "Mitekurenaito, abarechauzo!", que significa "¡Si no lo ves, haré algo violento!"

