                    El Mar del Caos. (混沌の海, konton no umi)
                </h2>
                <p class="">
                    La estructura del universo en Slayers es bastante peculiar. Para empezar la fuente y origen de toda la creación es el Mar del Caos o, dicho de otra manera, la deidad conocida como Lord of Nightmares. La manera más sencilla de explicar este concepto es pensar en el Mar del Caos como una masa infinita y sin forma, de la que surgen los distintos mundos de la creación, y que fluye en el espacio entre ellos. En origen, el Mar del Caos era todo y lo único que existía en la creación, un ser que lo abarcaba absolutamente todo hasta que, en algún momento de su existencia, surgieron los distintos mundos por algún motivo desconocido. Normalmente se cree que dichos mundos se encuentran sobre "bastones" o "columnas" que se elevan desde el Mar del Caos, pero puede que sea simplemente una forma de darle sentido al concepto en el imaginero popular. En dischos mundos, la raza de los Mazoku quieren destruirlo todo para volver al mar del caos, y las fuerzas del bien pelean por salvar su mundo. Cuando se habla de "mundos", lo más lógico es pensar que se refieren a universos, realidades o dimensiones completas, en vez de "planetas" del mismo universo.
