                    El número exacto de bastones / mundos es desconocido, pero bien podría ser infinito. El mundo de Flare Dragon Ceiphied y y Ruby Eye Shabranigudu es el que más se conoce, ya que es donde se desarrolla Slayers, del resto sólo conocemos unos cuantos datos, y en su mayoría sólo el nombre de sus Señores Oscuros, gracias a que su poder es invocado por el talismán Demon Blood. Del mundo de Night Dragon Volphied y Dark Star Dugradigudu no se tiene información de las novelas, excepto que Dark Star es el creador de la Espada de Luz y otras cuatro armas, pero en el anime se dan más datos. En esa versión Dark Star y Volphied se cansaron del continuo conflicto entre el bien y el mal, que había devastado su mundo, por lo que Volphied se dejó absorber por el Señor Oscuro, cuyas fuerzas salieron victoriosas. Más tarde ambos se encontrarían transportados al mundo de Ceiphied y derrotados a manos de los seres que allí habitan. Poco se sabe acerca de los dos mundos conocidos restantes, excepto que sus correspondientes Señores Oscuros tienen como títulos Death Fog, y Chaotic Blue.</p>
