                <h2 class="">
                El mundo de Ceiphied
                </h2>
                <p class="">
                En el material original, es decir, en las novelas, no se dan muchos datos del mundo de Ceiphied. Se sabe que es un universo muy similar al nuestro, al menos en apariencia. Existen estrellas en el cielo nocturno, un sol, y al menos un planeta, en el que suceden los hechos de Slayers, rodeado por un satélite natural, o luna, con fases. Se desconocen los distintos continentes que lo forman, pues sólo se conoce una pequeña zona en el interior de la Barrera Mazoku.

En cambio, en entrevistas con directivos del anime y demás material extra se dan más datos e imágenes. En esta versión el planeta es algo mayor que nuestra Tierra, con unos 7.000 kilómetros de diámetro. Cuenta con numerosas masas de tierra firme, aunque el área cubierta por el agua es mayor. Además existe lo que parece ser un continente helado que se encuentra en el polo sur. En la antigüedad existía un gran y único continente, con forma de dragón, cuyo centro quedó arrasado en la batalla entre Ceiphied y Shabranigudu, dicho lugar quedó marcado para siempre por una gigantesca columna de luz en lo que se conoce como la Tierra Prohibida.                </p>
